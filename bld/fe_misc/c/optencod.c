static CODESEQ *newCode( OPTION *o, char c, bool sensitive )
{
    CODESEQ *p;

    p = calloc( 1, sizeof( *p ) );
    p->option = o;
    p->c = c;
    p->sensitive = sensitive;
    return( p );
}

static CODESEQ *addOptionCodeSeq( CODESEQ *code, OPTION *o )
{
    bool    sensitive;
    char    *n;
    char    c;
    CODESEQ *head;
    CODESEQ **splice;

    head = code;
    splice = &head;
    for( n = o->name; (c = *n++) != '\0'; ) {
        sensitive = false;
        if( c == '\\' ) {
            c = *n++;
            sensitive = true;
        } else {
            c = mytolower( c );
        }
        for( code = *splice; code != NULL; code = *splice ) {
            if( code->sensitive == sensitive ) {
                if( code->c == c ) {
                    break;
                }
            }
            splice = &(code->sibling);
        }
        if( code == NULL ) {
            code = newCode( o, c, sensitive );
            *splice = code;
        }
        splice = &(code->children);
    }
    if( code == NULL ) {
        code = newCode( o, '\0', false );
        *splice = code;
    }
    code->accept = true;
    code->option = o;
    return( head );
}

static CODESEQ *reorderCode( CODESEQ *c )
{
    CODESEQ *a;
    CODESEQ *h;
    CODESEQ *n;
    CODESEQ **s;

    h = c;
    if( c->sibling != NULL ) {
        a = NULL;
        s = &h;
        // accepting states move to the end
        for( c = h; c != NULL; c = n ) {
            n = c->sibling;
            if( c->accept ) {
                *s = n;
                c->sibling = a;
                a = c;
            } else {
                s = &(c->sibling);
            }
        }
        *s = a;
        s = &(h->sibling);
        // sensitive states move to the front
        for( c = h; c != NULL; c = n ) {
            n = c->sibling;
            if( c->sensitive ) {
                *s = n;
                c->sibling = h;
                h = c;
            } else {
                s = &(c->sibling);
            }
        }
    }
    for( c = h; c != NULL; c = c->sibling ) {
        if( c->children != NULL ) {
            c->children = reorderCode( c->children );
        }
    }
    return( h );
}

static bool markChainCode( CODESEQ *h, size_t level )
{
    CODESEQ *c;
    bool    rc;

    rc = false;
    for( c = h; c != NULL; c = c->sibling ) {
        if( c->option->chain != NULL && c->option->chain->code_used ) {
            if( c->children != NULL ) {
                if( level == c->option->chain->clen ) {
                    if( markChainCode( c->children, level + 1 ) ) {
                        c->chain_root = true;
                    }
                }
            } else if( c->option->slen == c->option->chain->clen + 1 ) {
                c->chain = true;
                rc = true;
            }
        }
    }
    return( rc );
}

static CODESEQ *genCode( OPTION *o )
{
    CODESEQ *head;

    head = NULL;
    for( ; o != NULL; o = o->next ) {
        head = addOptionCodeSeq( head, o );
    }
    head = reorderCode( head );
    markChainCode( head, 1 );
    return( head );
}

static bool useSwitchStmt( CODESEQ *h )
{
    unsigned count;
    CODESEQ *c;

    count = 0;
    for( c = h; c != NULL; c = c->sibling ) {
        if( c->option->is_prefix ) {
            return( true );
        }
        ++count;
    }
    return( count >= USE_SWITCH_THRESHOLD );
}

