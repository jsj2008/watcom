static char *strpcpy( char *d, const char *s )
{
    size_t len;

    len = strlen( s );
    strcpy( d, s );
    return( d + len );
}

static char *special_char( char *f, char c )
{
    if( c == '~' ) {
        f = strpcpy( f, "_tilde" );
    } else if( c == '+' ) {
        f = strpcpy( f, "_plus" );
    } else if( c == '!' ) {
        f = strpcpy( f, "_exclamation" );
    } else if( c == '#' ) {
        f = strpcpy( f, "_sharp" );
    } else {
        *f++ = '_';
    }
    return( f );
}

static void makeFieldName( const char *n, char *f )
{
    char c;
    bool sensitive;
    bool special;

    c = *n++;
    if( c == '\\' ) {
        c = *n++;
    } else {
        c = mytolower( c );
    }
    if( c != '\0' ) {
        special = false;
        if( isalnum( c ) ) {
            if( isdigit( c ) )
                *f++ = '_';
            *f++ = c;
        } else {
            f = special_char( f, c );
            special = true;
        }
        sensitive = false;
        for( ; (c = *n++) != '\0'; ) {
            if( c == '\\' ) {
                sensitive = true;
                continue;
            }
            if( isalnum( c ) ) {
                if( special && *( f - 1 ) != '_' )
                    *f++ = '_';
                if( !sensitive )
                    c = mytolower( c );
                *f++ = c;
                special = false;
            } else {
                f = special_char( f, c );
                special = true;
            }
            sensitive = false;
        }
    }
    *f = '\0';
}

